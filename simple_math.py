first_number = int(input("What is your first number? "))
second_number = int(input("What is your second number? "))

sum = first_number + second_number
difference = first_number - second_number
product = first_number * second_number
quotient = first_number / second_number

print(first_number + "+" + second_number + "=" + sum)
print(first_number + "-" + second_number + "=" + difference)
print(first_number + "*" + second_number + "=" + product)
print(first_number + "/" + second_number + "=" + quotient)
